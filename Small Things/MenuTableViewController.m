#import "MenuTableViewController.h"
#import "DNTutorial.h"
#import "AppDelegate.h"

@interface MenuTableViewController ()

@property (weak, nonatomic) IBOutlet UISwitch *tutorialSwitch;

@end

@implementation MenuTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    
    if ([DNTutorial currentStep] != nil) {
        [self.tutorialSwitch setOn:NO animated:YES];
    }
}

- (void)viewWillDisappear:(BOOL)animated {
    [super viewWillDisappear:animated];
    
    if ([DNTutorial currentStep] != nil) {
        [self.tutorialSwitch setOn:NO animated:YES];
    }
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

- (IBAction)showTutorial:(UISwitch *)sender {
    AppDelegate *delegate = (AppDelegate*)[[UIApplication sharedApplication] delegate];
    
    if ([sender isOn]) {
        [DNTutorial resetProgress];
        delegate.first = YES;
    } else if (![sender isOn]){
        [DNTutorial setHidden:YES];
    }
}

